<?php

declare(strict_types=1);

namespace MaxMessenger\Bot\MaxBot\Event;

use MaxMessenger\Bot\Exception\MaxBot\Event\EventException;

/**
 * Вспомогательный класс для управления потоком обработки событий.
 *
 * Содержит статические методы для прерывания обработки событий и исключений
 * с возможностью управления статусом обработки (обработано/не обработано).
 *
 * Методы класса бросают {@see EventException} для немедленного завершения цепочки обработчиков:
 *
 * - {@see Event::break()} — завершает обработку **события** или **исключения**, помечая событие как обработанное
 * - {@see Event::continue()} — завершает обработку **события** или **исключения**, помечая событие как необработанное
 * - {@see Event::exit()} — завершает обработку **события** или **исключения** без изменения статуса
 */
final class Event
{
    /**
     * Прерывает обработку **события** или **исключения**, установить **событию** статус `обработано`.
     */
    public static function break(): never
    {
        throw new EventException(true);
    }

    /**
     * Прерывает обработку **события** или **исключения**, установить **событию** статус `не обработано`.
     */
    public static function continue(): never
    {
        throw new EventException(false);
    }

    /**
     * Прерывает обработку **события** или **исключения**, статус **события** не менять.
     */
    public static function exit(): never
    {
        throw new EventException();
    }
}
